/* Crie um script que calcula a área de um triangulo com vase na largura e altura fornecidas. A fórmula para cálculo da áreaé : Área = largura * Altura.*/
let largura = parseFloat(prompt("Informe a largura do triângulo: "));
let altura = parseFloat(prompt("Informe a altura do triângulo: "));
let area = (largura * altura) / 2;
console.log("A área do triângulo é:", area);
