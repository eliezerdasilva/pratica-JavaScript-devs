/*crie um script que converte uma temperatura em graus celsio para fahrenheit. A formula para conversão é : Fahrenheit = (Celsiius*9/5)+32.*/
let temperaturaCelsius = parseFloat(prompt("Digite a temperatura em graus Celsius: "));

let temperaturaFahrenheit = (temperaturaCelsius * 9/5) + 32;

console.log("A temperatura em graus Fahrenheit é:", temperaturaFahrenheit);

